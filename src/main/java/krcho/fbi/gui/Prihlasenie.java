    private class VytvorSessionFactory extends SwingWorker<Boolean, Integer> {

        private javax.swing.JFrame frame;

        private VytvorSessionFactory(javax.swing.JFrame frame) {
            this.frame = frame;
        }

        @Override
        protected Boolean doInBackground() throws Exception {
            spravaLabel.setText("Prebieha prihlasovanie...");
            String meno = menoTextField.getText();
            String heslo = String.copyValueOf(hesloPasswordField.getPassword());
            prihlasitButton.setEnabled(false);
            return HibernateUtilities.buildSessionFactory(meno, heslo);
        }

        @Override
        protected void done() {
            spravaLabel.setText("");
            prihlasitButton.setEnabled(true);
            try {
                if (get()) {
                    frame.dispose();
                    new TypovaPoziciaGui().setVisible(true);
                } else {
                    JOptionPane.showMessageDialog(frame, "Nespr치vne meno alebo heslo.", "Chyba!", JOptionPane.ERROR_MESSAGE);
                }
            } catch (InterruptedException ex) {
                Logger.getLogger(Prihlasenie.class.getName()).log(Level.SEVERE, null, ex);
            } catch (ExecutionException ex) {
                Logger.getLogger(Prihlasenie.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }
